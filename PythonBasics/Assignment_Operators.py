a = 5  # Equals

# Plus equals(+=)
a += 2  # shorthand for a = a + 2 => a is 7
print(a)

# Minus equals(+=)
a -= 3
print(a)

# Star equals(*=)
a *= 3
print(a)

# Slash equals (/=)
a /= 2  # a= a / 2 => a is 6
print(a)

# Double stars equals (**=)
a **= 2  # a = a ** 2 => a is 36

a += 1

# divmod()
a, b = divmod(14, 6)
print(a, b)

# pow()
print(pow(5, 9))

# sum ()
print(sum(1, 2, 3, 5, 6, 7, 7, 9))

# max()
print(max(1, 2, 3, 5, 6, 7, 7, 9))

# min()
print(min(1, 2, 3, 5, 6, 7, 7, 9))

# round()
a = 5.666772
print(round(a, 3))
b = round(a, 1)
print(a, b)


